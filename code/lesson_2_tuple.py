#与列表(list)不同之处
#列表使用中括号，元组使用小括号；
#元素的某个内容不能修改；不能排序：不能删除或增加某个元素；
#元组中只包含一个元素时，元素后加逗号

#元组创建：在括号中添加元素，并使用逗号隔开即可
tup1 =("apple","pear","banana")
tup2 =(1,2,3,4,5)
